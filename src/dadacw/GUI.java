    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton1ActionPerformed
    {//GEN-HEADEREND:event_jButton1ActionPerformed
        Scanner sc = new Scanner(getClass().getResourceAsStream("../res/items.csv"));
        String line;
        String[] itemString;

        if (sc.hasNextLine())
        {
            sc.nextLine();
        }
        //add data to stock
        while (sc.hasNextLine())
        {
            line = sc.nextLine();
            itemString = line.split(",");
            stock.addItem(itemString);
        }
        //get sets data in
        sc = new Scanner(getClass().getResourceAsStream("../res/sets.csv"));
        if (sc.hasNextLine())
        {
            sc.nextLine();
        }
        //add data to stock
        while (sc.hasNextLine())
        {
            line = sc.nextLine();
            itemString = line.split(",");
            stock.addSet(itemString);
        }
        
        this.updateGUI();
        //show and hide buttons
        if (stock.getItemsAsObjects().length > 0)
        {
            jButton1.setEnabled(false);
            jButton2.setEnabled(true);
            jButton3.setEnabled(true);
            jButton4.setEnabled(true);
            jButton5.setEnabled(true);
            //make buttons active
        }


    }//GEN-LAST:event_jButton1ActionPerformed

